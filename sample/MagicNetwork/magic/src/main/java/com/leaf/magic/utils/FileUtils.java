    public static void copyFile(File in, File out) {
        final int buffer_size = 1024;
        try {
            InputStream is = new FileInputStream(in);
            OutputStream output = new FileOutputStream(out);
            byte[] bytes = new byte[buffer_size];
            for (; ; ) {
                int count = is.read(bytes, 0, buffer_size);
                if (count == -1)
                    break;
                output.write(bytes, 0, count);
            }
            is.close();
            output.flush();
            output.close();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }

    public static String getPathFromUri(Context context, Uri uri) throws URISyntaxException {
        if ("content".equalsIgnoreCase(uri.getScheme())) {
            String[] projection = {"_data"};
            Cursor cursor = null;

            try {
                cursor = context.getContentResolver().query(uri, projection, null, null, null);
                int column_index = cursor.getColumnIndexOrThrow("_data");
                if (cursor.moveToFirst()) {
                    return cursor.getString(column_index);
                }
            } catch (Exception e) {
                // Eat it
            }
        } else if ("file".equalsIgnoreCase(uri.getScheme())) {
            return uri.getPath();
        }

        return null;
    }


    /**
     * Get MD5 of File.
     *
     * @param path File path
     * @return MD5 checksum on String.
     */
    public static String getMD5(String path) {
        long start = System.currentTimeMillis();
        String hash = null;
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            FileInputStream fis = new FileInputStream(path);
            byte[] dataBytes = new byte[1024];
            int nread = 0;
            while ((nread = fis.read(dataBytes)) != -1) {
                md.update(dataBytes, 0, nread);
            }

            byte[] mdbytes = md.digest();

            // convert the byte to hex format
            StringBuffer sb = new StringBuffer("");
            for (int i = 0; i < mdbytes.length; i++) {
                sb.append(Integer.toString((mdbytes[i] & 0xff) + 0x100, 16).substring(1));
            }
            hash = sb.toString();
        } catch (NoSuchAlgorithmException e) {
            e.printStackTrace();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
        Log.i("zhong", "md52:" + hash + " takes:" + (System.currentTimeMillis() - start));

        return hash;
    }

    public static String longSize2Str(long size) {
        if (size <= 0) {
            return "0";
        }

        double dSize = Double.valueOf(size);
        if (size >= 1073741824L) {
            return formatLongNum(dSize / Double.valueOf(1073741824L)) + "GB";
        } else if (size >= 1048576L) {
            return formatLongNum(dSize / Double.valueOf(1048576L)) + "MB";
        } else if (size >= 1024L) {
            return formatLongNum(dSize / Double.valueOf(1024L)) + "KB";
        } else {
            return size + "B";
        }
    }

    public static String formatLongNum(double num) {
        return new BigDecimal(num).setScale(1, RoundingMode.HALF_UP).toString();
    }

    /**
     * Choose a unique file name in download directory.
     *
     * @param fileName
     * @return File
     */
    public static File chooseUniqueFileName(File rootDir, String fileName) {
        File targetFile = new File(rootDir, fileName);
        if (!targetFile.exists()) {
            return targetFile;
        }

        // If file already exists, then make a new file name.
        String name = null;
        String extension = null;
        int dotIndex = fileName.lastIndexOf(".");
        if (dotIndex < 0) {
            name = fileName;
            extension = "";//FIXME get from MIME
        } else {
            name = fileName.substring(0, dotIndex);
            extension = fileName.substring(dotIndex, fileName.length());
        }

        // Add sequence.
        String fullFileName = null;
        for (int sequence = 1; sequence <= 10000; sequence++) {
            fullFileName = name + "-" + sequence + extension;
            targetFile = new File(rootDir, fullFileName);
            if (!targetFile.exists()) {
                break;
            }
        }

        return targetFile;
    }

    public static void clearDir(File rootDir) {
        if (rootDir != null && rootDir.exists()) {
            File[] files = rootDir.listFiles();
            if (files != null && files.length > 0) {
                for (File f : files) {
                    if (f.isDirectory()) {
                        clearDir(f);
                    } else {
                        f.delete();
                    }
                }
            }
        }
    }
